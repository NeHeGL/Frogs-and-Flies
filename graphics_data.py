# Helper function to get frog sprite data directly from ROM
def get_frog_sprite(hopping=False):
    """
    Returns the frog sprite data from ROM (stored upside down).
    
    Args:
        hopping: If True, returns the hopping/stretched sprite. If False, returns sitting sprite.
    """
    if hopping:
        # Hopping frog sprite - stretched out, legs extended (offset 582, read 16 bytes)
        return GRAPHICS_ROM[FROG_HOP_SPRITE_OFFSET:FROG_HOP_SPRITE_OFFSET + FROG_HOP_SPRITE_HEIGHT]
    else:
        # Sitting frog sprite - compact, legs tucked (offset 516, read 16 bytes)
        # Start one byte earlier (516 instead of 517) and read 16 bytes instead of 15
        return GRAPHICS_ROM[FROG_SPRITE_OFFSET - 1:FROG_SPRITE_OFFSET - 1 + 16]

